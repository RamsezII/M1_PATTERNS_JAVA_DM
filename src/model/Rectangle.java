package model;

public class Rectangle {
	private float height;
	private float width;
	
	public Rectangle(float height, float width) {
		this.height = height;
		this.width = width;
	}
	
	// Getters

	public float getHeight() {
		return this.height;
	}

	public float getWidth() {
		return this.width;
	}

	// Setters
	
	public void setHeight(float height) {
		this.height = height;
	}
	
	public void setWidth(float width) {
		this.width = width;
	}
	
	// Methods

}
